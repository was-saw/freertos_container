#define MY_LFS_DEBUG
int init_littlefs(lfs_t* lfs) {
    if (lfs == NULL) {
#ifdef MY_LFS_DEBUG
        xil_printf("lfs pointer is NULL!\r\n");
#endif
        return -1;
    }

    if (lfs_rambd_create(&cfg, &rambd_cfg) != 0) {
#ifdef MY_LFS_DEBUG
        xil_printf("lfs_rambd_create failed");
#endif
        return -1;
    }
    if (lfs_format(lfs, &cfg) != 0) {
#ifdef MY_LFS_DEBUG
        xil_printf("LittleFS format failed!\r\n");
#endif
        return -1;
    }
    if (lfs_mount(lfs, &cfg) != 0) {
#ifdef MY_LFS_DEBUG
        xil_printf("LittleFS mount failed!\r\n");
#endif
        return -1;
    }
#ifdef MY_LFS_DEBUG
    xil_printf("LittleFS mounted!\r\n");
#endif

    /* Write ls, cd, pwd ELF files to root directory */
    {
        lfs_file_t file;
        int ret;

        /* Write ls file */
#ifdef MY_LFS_DEBUG
        xil_printf("Writing /ls...\r\n");
#endif
        ret = lfs_file_open(lfs, &file, "/ls", LFS_O_WRONLY | LFS_O_CREAT | LFS_O_TRUNC);
        if (ret >= 0) {
            lfs_file_write(lfs, &file, ls, sizeof(ls));
            lfs_file_close(lfs, &file);
#ifdef MY_LFS_DEBUG
            xil_printf("/ls written (%llu bytes)\r\n", (unsigned long long)sizeof(ls));
#endif
        } else {
#ifdef MY_LFS_DEBUG
            xil_printf("Failed to write /ls\r\n");
#endif
        }

        /* Write cd file */
#ifdef MY_LFS_DEBUG
        xil_printf("Writing /cd...\r\n");
#endif
        ret = lfs_file_open(lfs, &file, "/cd", LFS_O_WRONLY | LFS_O_CREAT | LFS_O_TRUNC);
        if (ret >= 0) {
            lfs_file_write(lfs, &file, cd, sizeof(cd));
            lfs_file_close(lfs, &file);
#ifdef MY_LFS_DEBUG
            xil_printf("/cd written (%llu bytes)\r\n", (unsigned long long)sizeof(cd));
#endif
        } else {
#ifdef MY_LFS_DEBUG
            xil_printf("Failed to write /cd\r\n");
#endif
        }

        /* Write pwd file */
#ifdef MY_LFS_DEBUG
        xil_printf("Writing /pwd...\r\n");
#endif
        ret = lfs_file_open(lfs, &file, "/pwd", LFS_O_WRONLY | LFS_O_CREAT | LFS_O_TRUNC);
        if (ret >= 0) {
            lfs_file_write(lfs, &file, pwd, sizeof(pwd));
            lfs_file_close(lfs, &file);
#ifdef MY_LFS_DEBUG
            xil_printf("/pwd written (%llu bytes)\r\n", (unsigned long long)sizeof(pwd));
#endif
        } else {
#ifdef MY_LFS_DEBUG
            xil_printf("Failed to write /pwd\r\n");
#endif
        }

        /* Write image file */
#ifdef MY_LFS_DEBUG
        xil_printf("Writing /image...\r\n");
#endif
        ret = lfs_file_open(lfs, &file, "/image", LFS_O_WRONLY | LFS_O_CREAT | LFS_O_TRUNC);
        if (ret >= 0) {
            lfs_file_write(lfs, &file, image, sizeof(image));
            lfs_file_close(lfs, &file);
#ifdef MY_LFS_DEBUG
            xil_printf("/image written (%llu bytes)\r\n", (unsigned long long)sizeof(image));
#endif
        } else {
#ifdef MY_LFS_DEBUG
            xil_printf("Failed to write /image\r\n");
#endif
        }
    }

    return 0;
}
